// Validate that the param is a valid v4 ip address
// @param a string that will be tested in a regular expression to see if its a valid v4 ip address
function validateIpAdressParameter(ipAddress:string): boolean {
	return (/^(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/.test(ipAddress));
}
export default validateIpAdressParameter;